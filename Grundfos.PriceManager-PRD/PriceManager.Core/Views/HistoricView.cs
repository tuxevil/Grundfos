using System;
using System.Collections.Generic;
using System.Text;

namespace PriceManager.Core
{
    public class HistoricView
    {
        private double dateTicks;
        private decimal priceList;

        public HistoricView(double dateTicks, decimal val)
        {
            this.DateTicks = dateTicks;
            this.PriceList = val;
        }

        public double DateTicks
        {
            get { return dateTicks; }
            set { dateTicks = value; }
        }

        public decimal PriceList
        {
            get { return priceList; }
            set { priceList = value; }
        }
    }

}
