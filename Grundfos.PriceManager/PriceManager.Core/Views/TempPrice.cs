using System;
using System.Collections.Generic;
using System.Text;

namespace PriceManager.Core
{
    public class TempPrice
    {
        private string newPrice;
        private string originalPrice;
        private string newCtr;
        private string newPriceWF;
        private decimal originalPriceWF;

        public virtual string NewPrice
        {
            get { return newPrice; }
            set { newPrice = value; }
        }

        public virtual string OriginalPrice
        {
            get { return originalPrice; }
            set { originalPrice = value; }
        }

        public virtual string NewCtr
        {
            get { return newCtr; }
            set { newCtr = value; }
        }

        public virtual string NewPriceWF
        {
            get { return newPriceWF; }
            set { newPriceWF = value; }
        }

        public decimal OriginalPriceWF
        {
            get { return originalPriceWF; }
            set { originalPriceWF = value; }
        }
    }
}
